function deepFill(base, partial) {
  const out = structuredClone(base);
  function walk(o, p) {
    if (!p || typeof p !== "object") return;
    for (const k of Object.keys(p)) {
      const pv = p[k];
      const ov = o[k];
      if (pv && typeof pv === "object" && !Array.isArray(pv) && ov && typeof ov === "object" && !Array.isArray(ov)) {
        walk(ov, pv);
        o[k] = ov;
      } else {
        o[k] = pv;
      }
    }
  }
  walk(out, partial);
  return out;
}

